package basicJava;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int a=10;
		int b=5;
		System.out.println("a+b = "+(a+b));
		System.out.println("a-b = "+(a-b));
		System.out.println("a*b = "+(a*b));
		System.out.println("a/b = "+(a/b));
		System.out.println("a%b = "+(a%b));		
		a++;
		System.out.println(a);
		b--;
		System.out.println(b);
		
		int c=10;
		c+=5;   //c=c+5
		System.out.println(c);
		
	}
}
